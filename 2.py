# Створення телефонної книги — список словників
phone_book = [
    {"ім'я": "Іван", "прізвище": "Іванов", "телефон": "123456789", "місто": "Київ"},
    {"ім'я": "Марія", "прізвище": "Петренко", "телефон": "987654321", "місто": "Львів"},
    {"ім'я": "Олег", "прізвище": "Сидоренко", "телефон": "555123456", "місто": "Одеса"},
    {"ім'я": "Анна", "прізвище": "Мельник", "телефон": "456789123", "місто": "Харків"},
    {"ім'я": "Юрій", "прізвище": "Шевченко", "телефон": "321654987", "місто": "Дніпро"}
]

# Виведення телефонної книги у вигляді таблиці
print("Ім'я\t\tПрізвище\t\tТелефон\t\t\tМісто")
for contact in phone_book:
    print(f"{contact['ім\'я']}\t\t{contact['прізвище']}\t\t{contact['телефон']}\t\t{contact['місто']}")

# Пошук контактів за ім'ям, прізвищем або містом
search_key = input("Введіть параметр пошуку (ім'я, прізвище або місто): ").strip()

# Перевірка, чи рядок не порожній
if search_key == "":
    print("Помилка: Ви нічого не ввели.")
else:
    found_contacts = []

    # Перевірка кожного контакту
    for contact in phone_book:
        if (search_key.lower() in contact["ім'я"].lower() or
            search_key.lower() in contact["прізвище"].lower() or
            search_key.lower() in contact["місто"].lower()):
            found_contacts.append(contact)

    # Результат пошуку
    if found_contacts:
        print("Знайдено контакти:")
        for c in found_contacts:
            print(f"{c['ім\'я']} {c['прізвище']}, Телефон: {c['телефон']}, Місто: {c['місто']}")
    else:
        print("Контактів за таким запитом не знайдено.")
